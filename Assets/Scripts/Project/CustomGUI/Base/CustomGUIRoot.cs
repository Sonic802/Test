using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[ExecuteAlways]
public class CustomGUIRoot : MonoBehaviour
{
    //用于存储子对象 所有的GUI控件的容器
    private CustomGUIControl[] allControls;
    
    void Start()
    {
        allControls = GetComponentsInChildren<CustomGUIControl>();
    }

    //此处统一绘制子对象控件的内容
    private void OnGUI()
    {
        //每一次绘制之前 得到所有子对象控件的 父类脚本
        //代码每次执行OnGUI都会去获取所有控件对应的脚本
        //浪费性能的解决方式：编辑状态下一直执行，游戏开始后只需要在Start函数中执行一次即可

        allControls = GetComponentsInChildren<CustomGUIControl>();
        
       

        //遍历每一个控件 让其执行绘制
        for (int i = 0; i < allControls.Length; i++)
        {
            allControls[i].DrawGUI();
        }
    }
}
